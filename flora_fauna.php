<h1 id="heading">Flora and Fauna around Mount Santubong</h1>

<p> <img class="floatleft" src="images/santubong_rainforest.jpg" alt="Santubong rainforest" title="Santubong rainforest"  width="250px" height="200px"/> With only 35 minutes drive from Kuching City, the area that surrounds Mt. Santubong offers a great deal to the visitors, environment lovers and researchers. It contains an awesome natural attractions which centred on the beautiful rainforest slopes of Mount Santubong, its mangrove forest, mudflats and rivers. It is an ideal place for researchers to conduct research.</p>



<p class="text_line"> <img class ="floatright" src="images/irrawaddy-dolphin-1.jpg" alt="Irrawaddy dolphins in Santubong"  title="Irrawaddy dolphins" width="250px" height="200px"/> Apart from that, these different habitats provide homes to a variety of wildlife such as Rhinoceros Hornbill, Collared Kingfisher, White-bellied Sea Eagle, Proboscis monkey and many more. Thus, this make Santubong one of the best sites in Sarawak to enjoy the scenery of wildlife in a natural way. If you are lucky enough, Finless porpoises and Indo-pacific humpback dolphins can be spotted in the water around Santubong; also the rare Irrawaddy dolphins can be seen in shallow coastal areas.</p>




<!--This information is referred from the Borneo post (http://www.theborneopost.com/2012/06/24/plans-for-kuching-delta-mt-kinabalu-to-become-unesco-geoparks/) and Parochial Sarawakian (http://parochialsarawakian.wordpress.com/2012/03/24/sarawak-works-to-gain-unesco-geopark-status-kuching/)-->
<p>Due to the blessings given to Santubong and its surrounding areas, the Ministry of Tourism, National University of Malaysia (<em>Universiti Kebangsaan Malaysia</em>) and Geoscience Department in Kuching are planning to acquire the UNESCO Global Geopark status and become the second Global Geopark in Malaysia together with Mount Kinabalu which is in Sabah.</p>

