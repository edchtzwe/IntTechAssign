<h1 id="heading">Legend of Mount Santubong</h1>

<p>Although Mount Santubong is well known for its riches in both flora and fauna for researchers but did you know that it does contain a beautiful legend??</p>

<p>If you view the mountain from various locations in different angle such as Kuching city, Matang or even Kota Samarahan, you will noticed that in some angle the mountain resembles a women lying on her back.</p>

<p><strong><em>There is when the legend of Princess Santubong and Princess Sejinjang kicks in!!!</em></strong></p> 

<p>According to the locals, the legend begins with the two princesses once lived in the heaven's kingdom called Kayangan. Princess Santubong was well known for weaving skills because she was able to make the most beautiful cloth in Kayangan while Princess Sejinjang was known for pounding the tastiest rice.</p>

<p>One day, there was a quarrel between the two villages, <em>Pasir Kuning</em> (Yellow Sand) and <em>Pasir Putih</em> (White Sand) and the quarrel worsened each day. The King of Kayangan saw the situation and he sent both princesses to earth to restore peace between the villages and with a strict condition that both of them must never quarrel with one another.</p>

<p>Once they reach the earth, Princess Santubong rule over Pasir Kuning while Princess Sejinjang rule over Pasir Putih and because of their talents, both the villages were busy prospering from clothes and rice and they had no time to fight. Thus, both villages were at peace with one another.</p>

<p>Because of their beauty and talents, many suitors from near and far tried to propose to them. No one won their hearts until they met <em>Putera Makota Serapi</em> (Crown Prince Mahkota) from Matang. He was so handsome that both princesses fell in love with the prince at the same time.</p>

<p>The prince asked both to be his wives but they refused and started to quarrel. After verbally abusing, they started to fight with one another. Princess Sejinjang took thresher and hit Santubong's cheek which make her fell flat on her back and Princess Santubong hit back using her <em>belidak</em>, a tool which she weave cloths.</p>

<p>The <em>belidak</em> that hit Princess Sejinjang's head, broke her head into million pieces and scattered into the ocean and became the small islands around Kuching such as Pulau Kera, Pulau Satang, Pulau Lakir and other islands. When the King of Kayangan saw it, he was so angry that he cursed both of them into mountains thus putting the fight to an end.</p>

<p>It is said that the deep crevice at the peak of Mount Santubong is where the princess got hit on the cheek.</p>

