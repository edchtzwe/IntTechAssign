	<h1>History of Mount Santubong</h1>
	
	<img class="mountpict" src="./images/mount santubong.png" alt="Mount Santubong"/>
	<p>Mount Santubong (Malay: Gunung Santubong) is a mountain in the Malaysian state of Sarawak. It is located about 35 km north of the state capital Kuching.
	On a clear day, it can be seen from Kuching. The mountain and its surrounding area is a popular tourist attraction.</p>
	<p>According to the Encyclopaedia of Iban Studies</p> 
		<p><span id="indent">the original inhabitants of Santubong were the Iban. Si-antu-ubong means 'spirit boat' in the Iban language. Antu is hantu in Malay which means spirit or ghost. Santubong are boat like coffins made from a single hollow log designed to represent the vesell in which a dead person will travel from this world to afterlife. Following another theory, the name Santubong is derived from "san choo bong" in the Hakka Chinese dialect, meaning "wild pig king" or "king of wild pig".</span></p>
	
	<p>Excavations of the surrounding area uncovered Hindu and Buddhist relics from the 9th Century CE. Song and Tang dynasty ceramics are also found, indicating that the area around the mountain was a trading port from the 11th to 13th century.</p>
	
