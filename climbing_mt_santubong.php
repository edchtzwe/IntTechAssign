<h2>Item and equipment</h2>

<p>Here's a list of item and equipment that every climber should carry: </p>
<ul>
	<li>Good walking boots (<strong>Sandals are not recommended!!!</strong>)</li>
	<li>Waterproof jacket</li>
	<li>Warm and lightweight t-shirt</li>
	<li>Warm and lightweight pants</li>
</ul>

<p>Miscellaneous</p>
<ul>
	<li>Water bottles (At least one litre, small bottles are recommended)</li>
	<li>Snacks for the climb (Chocolate, nuts and jelly beans are recommended)</li>
	<li>Fruits (Apple or banana)</li>
	<li>Torchlight and spare batteries (In case of emergency)</li>
	<li>Whistle (in case of emergency)</li>
	<li>Small first aid Kit:-
		<ul>
			<li>Headache tablets</li>
			<li>Antiseptic cream</li>
			<li>Crepe bandage</li>
			<li>Triangular bandage</li>
			<li>Safety pins</li>
		</ul>
	</li>
	<li>Mosquito repellent</li>
</ul>

</div>

