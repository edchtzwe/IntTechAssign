<h1 id="heading">Hike/climb up to peak of Mt. Santubong (840m)</h1>

<div>

<p>Wish to climb Mt. Santubong but don't know where to start??</p>
<p>What is the duration of the climb??</p>
<p>What to prepare for the climb??</p>

</div>

<p><strong>NO WORRIES!!!</strong>  We've got you covered~~</p>

<p>If you plan to climb Mt. Santubong, the best time would be in the early morning. The duration of the whole climb would take around 7 hours which 3-4 hours to reach the summit and another 2-3 hours to reach the starting point. There are two starting points that reach to the summit of Santubong which are: Green Paradise Seafood Restaurant near Damai Golf and Country Resort and Trek Gunung Santubong near <em>Bukit Puteri.</em> </p>

<p>The price for climbing Mt. Santubong will be as follows:-</p>

<ol>
	<li>Green Paradise Seafood Restaurant
		<ul>
			<li>Price: RM5 per transport (if you have), RM 3 per hiker, and RM13 per certificate.
			</li>
		</ul>
	</li>
	<li>Trek Gunung Santubong
		<ul>
			<li>Price: Free of charge!! But there is no certificate given. 
			</li>
		</ul>
	</li>
</ol>

